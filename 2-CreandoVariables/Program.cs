using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _2_CreandoVariables
{
    class Program
    {
        static void Main(string[] args)
        {
            int edad;

            edad = 45;

            Console.WriteLine("Edad 1 " + edad);

            edad = 45 + 10;

            Console.WriteLine("Edad 2 " + edad);

            edad = (45 + 10) * 2;

            Console.WriteLine("Edad 3 " + edad);

            Console.ReadLine();

        }
    }
}
